<?php
/*

+ untuk tambah
- untuk kurang
* untuk kali
/ untuk bagi
% modulus atau sisa bagi
** untuk perpangkatan (exponent)

*/

//cara menampilkannya
echo 10 + 10; // hasilnya adalah 20
echo "<br>"; 
echo 40 - 20; //hasilnya adalah 20
echo "<br>";
echo 10 * 2; //hasilnya adalah 20
echo "<br>";
echo 40 / 2; //hasilnya adalah 20
echo "<br>";
echo 4 % 2; //hasilnya adalah 0
echo "<br>";
echo 100 ** 4; //hasilnya 100000000
?>